    public class EvenNumbers {
    public static void main(String[] args) {
        System.out.println("Even numbers between 20 and 100:");

        // Start at 20, continue while i is <= 100, increment by 2
        for (int i = 0; i <= 100; i += 2) {
            System.out.print(i + " ");
        }
    }
}

